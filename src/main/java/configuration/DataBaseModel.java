package configuration;

public class DataBaseModel {
    public String dbName;
    public String host;
    public String user;
    public String dbPassword;

    public String getDbName() {
        return dbName;
    }

    public String getHost() {
        return host;
    }

    public String getUser() {
        return user;
    }

    public String getDbPassword() {
        return dbPassword;
    }
}
